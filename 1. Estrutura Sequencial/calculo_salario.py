#Faça um Programa que pergunte quanto você ganha por hora e o número de horas trabalhadas no mês. Calcule e mostre o total do seu salário no referido mês.
print("Oi, calculo seu ganho mensal.")
print("Pra isso preciso do seu ganho por hora.")
print("E quantas horas tu trabalho no mes")
gain_hr = int(input("Manda ai seu ganho por hora"))
hr_mes = int(input("E aqui quantas horas você trabalha no mês, seu escravo do capitalismo"))
gain_mes = gain_hr * hr_mes
print("É, ate que ta bom pra um escravo")
print("Tu ganha",gain_mes,"pilas por mes")
print("Nice!")